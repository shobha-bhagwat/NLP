CREATE TABLE `T_WATERMARK` (
  `Curr_Watermark` TIMESTAMP NOT NULL,
  `Prev_Watermark` TIMESTAMP NOT NULL,
  `Updated_At` TIMESTAMP DEFAULT CURRENT_TIMESTAMP
) ENGINE=InnoDB DEFAULT CHARSET=latin1;


