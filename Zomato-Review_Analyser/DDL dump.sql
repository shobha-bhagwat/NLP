  `Review_Count` int(11) NOT NULL,
  `Negative_Count` int(11) NOT NULL,
  `Positive_Count` int(11) NOT NULL,
  `Email_Sent_Flag` CHAR(1) DEFAULT 'N',
  `Processed_At` TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
